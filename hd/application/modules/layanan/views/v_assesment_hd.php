function hitung_hpl()
{
  var tgl=$('#TGL_HPHT').val().split('/');
  var tahun=tgl[2];
  var bulan=tgl[1];
  var hari=tgl[0];

  var h=hari;
  var b=bulan;
  var t=tahun;

  if(parseInt(bulan)>=1 && parseInt(bulan)<=3)
  {
    t=tahun;
    b=parseInt(bulan)+9;
    h=parseInt(hari)+7;
  }
  else if(parseInt(bulan)>=4 && parseInt(bulan)<=12)
  {
    t=parseInt(tahun)+1;
    b=parseInt(bulan)-3;
    h=parseInt(hari)+7;
  };


  if(h.toString().length==1)
  {
    h='0'+h;
  };
  if(b.toString().length==1)
  {
    b='0'+b;
  };


  $('#TGL_HPL').val(h.toString()+'/'+b.toString()+'/'+t.toString());
}


